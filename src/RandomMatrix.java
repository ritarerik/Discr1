
// создание матрицы с рандомными элементами 

public class RandomMatrix {
	
	public static int[][] createINT(int size) {
		
		int A[][] = new int[size][size];
		
		
		return A;
		
	}
	
	public static boolean[][] createBOOLEAN(int size) {
		
		boolean A[][] = new boolean[size][size];
		
		
		return A;
		
	}
	
	
}
